package Gen1802;

public class Operadores
{


    public static void main(String[] args)
    {
        /*
        + suma
        - resta
        * multiplicacion
        / division
        % resto
         */

        int a = 10;
        int b = 5;
        final int c = 15;

        int resultado = 10 + 5;
        System.out.println(resultado);

        resultado = resultado - a;
        System.out.println(resultado);

        resultado = resultado * b;
        System.out.println(resultado);

        resultado = c/b;
        System.out.println(resultado);

        resultado = 10%2;
        System.out.println(resultado);



    }


}
